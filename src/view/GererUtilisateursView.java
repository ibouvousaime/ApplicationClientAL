    private void btnModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifierActionPerformed
        // TODO add your handling code here:
        
        int ligne = jtUtilisateurs.getSelectedRow();
        String idText = jtUtilisateurs.getValueAt(ligne, 0).toString();
        int id = Integer.parseInt(idText);
        Utilisateur utilisateur =new Utilisateur();
        utilisateur.setId(id);
        utilisateur.setLogin(txtLogin.getText());
        utilisateur.setPassword(txtPassword.getText());
        utilisateur.setPrenom(txtPrenom.getText());
        utilisateur.setNom(txtNom.getText());
        utilisateur.setProfil(cbProfil.getSelectedItem().toString());
        
        BDSchool bd = new BDSchool();
        boolean result = bd.modifierUtilisateur(utilisateur);
        if(result)
        {
            viderChamp();
            JOptionPane.showMessageDialog(this, "Utilisateur modifié", "Utilisateur", JOptionPane.INFORMATION_MESSAGE);
            chargerToutUtilisateurs();
        }
        else{
            
           JOptionPane.showMessageDialog(this, "Utilisateur non modifié", "Utilisateur", JOptionPane.WARNING_MESSAGE);

        }
    }//GEN-LAST:event_btnModifierActionPerformed

    private void btnSupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSupprimerActionPerformed
        
        int choix = JOptionPane.showConfirmDialog(this, "Voulez-vous supprmier cet utilisateur", "Suppression", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        
        if(choix == JOptionPane.YES_OPTION){
        int ligne = jtUtilisateurs.getSelectedRow();
        String idText = jtUtilisateurs.getValueAt(ligne, 0).toString();
        int id = Integer.parseInt(idText);
        BDSchool bd = new BDSchool();
        boolean result = bd.supprimerUtilisateur(id);
        if(result)
        {
            viderChamp();
            JOptionPane.showMessageDialog(this, "Utilisateur supprimé", "Utilisateur", JOptionPane.INFORMATION_MESSAGE);
            chargerToutUtilisateurs();
        }
        else{
            
           JOptionPane.showMessageDialog(this, "Utilisateur non supprimé", "Utilisateur", JOptionPane.WARNING_MESSAGE);

        }
        }
    }//GEN-LAST:event_btnSupprimerActionPerformed

