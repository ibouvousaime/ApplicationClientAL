    private void btnAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterActionPerformed
        // TODO add your handling code here:
        
        String prenom = txtPrenom.getText().toString().trim();
        String nom =txtNom.getText().toString().trim();
        String login = txtLogin.getText().toString().trim();
        String password =txtPassword.getText().toString().trim();
        String confirmer= txtConfirmer.getText().toString().trim();
        String profil = cbProfil.getSelectedItem().toString();
        
        if(prenom.equals("")|| nom.equals("") || login.equals("")
                || password.equals("") || confirmer.equals(""))
        {
            JOptionPane.showMessageDialog(this, "Veuiller renseigner les champs", "Erreur de saisie", JOptionPane.WARNING_MESSAGE);
        }
        else 
        {
            if(password.equals(confirmer)==false)
            {
                JOptionPane.showMessageDialog(this, "Les mots de passe ne sont pas identiques", "Mot de passe", JOptionPane.WARNING_MESSAGE);
            }
            else
            {
                //creation d'un nouvel object utilisateur
                Utilisateur utilisateur = new Utilisateur();
                utilisateur.setLogin(login);
                utilisateur.setPassword(password);
                utilisateur.setPrenom(prenom);
                utilisateur.setNom(nom);
                utilisateur.setProfil(profil);
                
                BDSchool bd = new BDSchool();//creer un object connection
                boolean result = bd.ajouterUtilisateur(utilisateur);//appel de la méthode pour inserer l'utilisateur dans la table
                
                if(result){
                    //Vider ls champs
                    viderChamp();
                    JOptionPane.showMessageDialog(this, "Utilisateur enregistré", "Utilisateur", JOptionPane.INFORMATION_MESSAGE);
                
                    chargerToutUtilisateurs();
                
                }
                else{
                   JOptionPane.showMessageDialog(this, "Utilisateur non enregistré", "Utilisateur", JOptionPane.WARNING_MESSAGE);

                }
                
                //Verification saisie Ok, Verifier si le user n'est pas deja enregistré
                //Si nouveau utilisateur, Insertion dans la table user
            }
        }
        
    }//GEN-LAST:event_btnAjouterActionPerformed

